#import "ServiceItem.h"

@implementation ServiceItem

@synthesize name, status, duration;
-(NSString*)name {
	return name;
}
-(NSString*)status {
	return status;
}
-(NSUInteger*)duration {
	return duration;
}
-(void)setName:(NSString *)input{
	[name autorelease];
	name = [input retain];
}
-(void)setStatus:(NSString *)input{
	[status autorelease];
	status = [input retain];
}

@end
